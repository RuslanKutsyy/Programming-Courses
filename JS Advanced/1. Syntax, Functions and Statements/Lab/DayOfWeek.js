function dayOfWeek(arg) {
    if (arg === 'Monday') {
        console.log(1);        
    } else if (arg === 'Tuesday') {
        console.log(2);        
    } else if (arg === 'Wednesday') {
        console.log(3);        
    } else if (arg === 'Thursday') {
        console.log(4);        
    } else if (arg === 'Friday') {
        console.log(5);        
    } else if (arg === 'Saturday') {
        console.log(6);        
    } else if (arg === 'Sunday') {
        console.log(7);        
    } else {
        console.log('error');        
    }
}

dayOfWeek();